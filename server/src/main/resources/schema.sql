CREATE SCHEMA shareit;

CREATE TABLE IF NOT EXISTS shareit.users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username CHARACTER VARYING(100) NOT NULL,
    email CHARACTER VARYING(100) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS shareit.request (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    description CHARACTER VARYING(200) NOT NULL,
    created TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    requester_id BIGINT NOT NULL REFERENCES shareit.users (id)
    );

CREATE TABLE IF NOT EXISTS shareit.item (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    itemname CHARACTER VARYING(100) NOT NULL,
    description CHARACTER VARYING(200) NOT NULL,
    available BOOLEAN,
    owner_id BIGINT NOT NULL REFERENCES shareit.users (id),
    request_id BIGINT REFERENCES shareit.request (id)
);

CREATE TABLE IF NOT EXISTS shareit.booking (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    start_booking TIMESTAMP WITHOUT TIME ZONE,
    end_booking TIMESTAMP WITHOUT TIME ZONE,
    status CHARACTER VARYING(10) NOT NULL,
    booker_id BIGINT NOT NULL REFERENCES  shareit.users (id),
    item_id BIGINT NOT NULL REFERENCES shareit.item (id)
);

CREATE TABLE IF NOT EXISTS shareit.comment (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    text CHARACTER VARYING(200) NOT NULL,
    created TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    item_id BIGINT NOT NULL REFERENCES shareit.item (id),
    author_id BIGINT NOT NULL REFERENCES shareit.users (id)
);